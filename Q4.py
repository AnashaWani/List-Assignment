# Using For Loop on the List created in Q1 :

list=["python",1,2.4,"git",48,8.7,"Green",11,-67.12,"info",0,0.0,"Kashmir",508,88.0]

# To display only the value of the element :
for x in list :
    print(x)

# To display index with the value of the element :
for i , v in enumerate(list) :
    print(f"Index:{i} , Value:{v}")